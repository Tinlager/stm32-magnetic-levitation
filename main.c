/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_ADCEx_Calibration_Start(&hadc1);
  HAL_ADC_Start(&hadc1);
  // Отримуємо початкове значення для фільтра
    if (HAL_ADC_PollForConversion(&hadc1, 10) == HAL_OK) {
        filtered_adc = (float)HAL_ADC_GetValue(&hadc1);
    }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // 1. Оверсемплінг
	        uint32_t sum_adc = 0;
	        for (int i = 0; i < 64; i++) {
	            HAL_ADC_Start(&hadc1);
	            if (HAL_ADC_PollForConversion(&hadc1, 1) == HAL_OK) {
	                sum_adc += HAL_ADC_GetValue(&hadc1);
	            }
	        }
	        avg_raw = (float)sum_adc / 64.0f;

	        // 2. ВРАХОВУЄМО ІНВЕРСІЮ: обчислюємо реальну потужність котушки (0 - вимкнена, 399 - 100%)
	        float real_duty = (float)pid_out;

	        // 3. Математична модель індуктивності котушки
	        float pwm_alpha = 0.05f;
	        smooth_pwm = (pwm_alpha * real_duty) + ((1.0f - pwm_alpha) * smooth_pwm);

	        // 4. НОВА КОМПЕНСАЦІЯ: котушка тягне АЦП вниз.
	        // Коефіцієнт = 34 / 399 = 0.085. ДОДАЄМО, щоб компенсувати просадку!
	        comp_adc = avg_raw + (smooth_pwm * 0.085f);

	        // 5. Легкий фінальний фільтр АЦП
	        float alpha = 0.1f;
	        filtered_adc = (alpha * comp_adc) + ((1.0f - alpha) * filtered_adc);

	        // 6. Розрахунок помилки
	        float error = filtered_adc - (float)setpoint;

	  // 7. Інтеграл
	  integral += error;
	  if (integral > 500.0f) integral = 500.0f;
	  if (integral < -500.0f) integral = -500.0f;

	  // 8. ПІД-регулятор (з фільтром швидкості)
	  float p_term = error * Kp;

	  // Замість того, щоб бити на всі гроші при найменшому шумі, ми згладжуємо ривки:
	  float raw_d = error - last_error;
	  filtered_d = (0.1f * raw_d) + (0.9f * filtered_d);
	  float d_term = filtered_d * Kd;

	  float i_term = integral * Ki;

	  pid_out = (int32_t)(p_term + d_term + i_term);
	  last_error = error;

	  // 9. Обмеження
	  if (pid_out > 399) pid_out = 399;
	  if (pid_out < 0) pid_out = 0;

	  // 10. Вивід
	  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 399 - pid_out);
	  HAL_Delay(1);
	      /* USER CODE END WHILE */
	      /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

